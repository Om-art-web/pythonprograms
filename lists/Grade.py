#  90 to 100 = A grade   80 to 90 = B   If Exact 90  then the B grade

#  80 to 50 = C Grade    50 to 30 = D Grade
#  45, 78, 99  



marks=int(input("Enter the Marks = "))

if marks>100 or marks<0:
    print("Invalid Marks")
elif marks>90:
    print("A Grade")
elif  marks>80:
    print("B Grade")
elif marks>50:
    print("C Grade")
else:
    print("D Grade")









